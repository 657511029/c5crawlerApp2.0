package com.example.update.entity;

public class UserInfo {
    private String userName;

    private String uuAccount;

    private String uuPassword;

    private String scale1;

    private String scale2;

    private String scale3;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUuAccount() {
        return uuAccount;
    }

    public void setUuAccount(String uuAccount) {
        this.uuAccount = uuAccount;
    }

    public String getScale1() {
        return scale1;
    }

    public void setScale1(String scale1) {
        this.scale1 = scale1;
    }

    public String getScale2() {
        return scale2;
    }

    public void setScale2(String scale2) {
        this.scale2 = scale2;
    }

    public String getScale3() {
        return scale3;
    }

    public void setScale3(String scale3) {
        this.scale3 = scale3;
    }

    public String getUuPassword() {
        return uuPassword;
    }

    public void setUuPassword(String uuPassword) {
        this.uuPassword = uuPassword;
    }
}
